//C Program to Print Alphabets From A to Z Using Loop
#include <stdio.h>

int main()
{
    //printf("Hello world!\n");

    for(char i=65;i<=90;i++)
    {
       printf("%c ",i);
    }
    printf("\n");
    for(char i=97;i<=122;i++)
    {
       printf("%c ",i);
    }

    return 0;
}
